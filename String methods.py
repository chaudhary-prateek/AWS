txt = "hey I'm Prateek Chaudhary"
# Make 1st letter capital only
x = txt.capitalize() 
print(x)

# make all small letters
y = txt.casefold()
print(y)

# center the string 
z = txt.center(50)
print(z)

# count 
a = txt.count("Prateek")
print(a)

# encode
hey = "how are you my jan !"
c = hey.encode()
print(c)